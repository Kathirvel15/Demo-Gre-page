//Contact Form Functionality
function namevalidation() {
  var getname = document.querySelector("#name");
  var geterror = document.querySelector(".name-error");
  //icon
  var getnameicon = document.querySelector(".nameicon");
  if (getname.value == "") {
    geterror.innerHTML = "Name is requried";
    getname.style.border = "2px solid red";
    getname.style.outline = "none";
    getnameicon.innerHTML = "";
    return false;
  }
  if (getname.value.length < 3) {
    geterror.innerHTML = "Name is requried";
    return false;
  }
  if (!getname.value.match(/^[a-zA-Z]/)) {
    geterror.innerHTML = "Digits are not allowed";
    getnameicon.innerHTML = "";
    return false;
  }
  geterror.innerHTML = "";
  getname.style.border = "2px solid #3e3c3cb2";
  getnameicon.innerHTML =
    '<img src="Assets/laptop-and-tick-shows-online-success.jpg" width="28px">';
  return true;
}
// Email id Validation
function emailvalidation() {
  var getname1 = document.querySelector("#email");
  var geterror1 = document.querySelector(".email-error");
  //icon
  var getnameicon1 = document.querySelector(".emailicon");
  if (
    !getname1.value.match(
      /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/
    )
  ) {
    geterror1.innerHTML = getname1.value + " is not valid";
    getname1.style.border = "2px solid red";
    getnameicon1.innerHTML = "";
    return false;
  }
  geterror1.innerHTML = "";
  getname1.style.border = "2px solid #3e3c3cb2";
  getnameicon1.innerHTML =
    '<img src="Assets/laptop-and-tick-shows-online-success.jpg" width="28px">';
  return true;
}
